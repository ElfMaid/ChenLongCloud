package com.chenlong.chehaobin.entity;

public class Role {
	
	private String role_nam;
	private String role_ID;
	private int level;
	public String getRole_nam() {
		return role_nam;
	}
	public void setRole_nam(String role_nam) {
		this.role_nam = role_nam;
	}
	public String getRole_ID() {
		return role_ID;
	}
	public void setRole_ID(String role_ID) {
		this.role_ID = role_ID;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}

	
	
	
	
	
	
	
}
