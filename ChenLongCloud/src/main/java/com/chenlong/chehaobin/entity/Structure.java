package com.chenlong.chehaobin.entity;

public class Structure {

	private String structure_nam;
	private String structure_ID;
	private int level;
	
	public String getStructure_nam() {
		return structure_nam;
	}
	public void setStructure_nam(String structure_nam) {
		this.structure_nam = structure_nam;
	}
	public String getStructure_ID() {
		return structure_ID;
	}
	public void setStructure_ID(String structure_ID) {
		this.structure_ID = structure_ID;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	
	
	
	
	
	
}
